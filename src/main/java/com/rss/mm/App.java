    private void initTableModel(List<Song> songs) {
        /* 1. Crear el modelo la primera vez (evita hacerlo en cada refresco) */
        if (tableModel == null) {
            tableModel = new DefaultTableModel(COLUMNAS, 0) {
                @Override public boolean isCellEditable(int row, int col) { return false; }
            };
            jTableSongs.setModel(tableModel);
        }

        /* 2. Batch-update: evita eventos por cada fila */
        tableModel.setRowCount(0);          // limpia tabla
        if (songs == null || songs.isEmpty()) return;

        Object[] row = new Object[3];       // reusable
        for (Song s : songs) {
            row[0] = s.getTitle();
            row[1] = s.getArtist();
            row[2] = s.getVotes();
            tableModel.addRow(row);         // O(1) amortizado
        }
    }
    

    private Song getSelectedSong() {
        int vRow = jTableSongs.getSelectedRow();
        if (vRow == -1) return null;
        int modelRow = jTableSongs.convertRowIndexToModel(vRow); // por si está ordenado
        String tit = (String)  tableModel.getValueAt(modelRow, 0);
        String art = (String)  tableModel.getValueAt(modelRow, 1);
        int    vot = (Integer) tableModel.getValueAt(modelRow, 2);
        // buscamos el objeto con esos datos (id oculto)
        for (Song s : operations.getSongs())
            if (s.getTitle().equals(tit) && s.getArtist().equals(art) && s.getVotes() == vot)
                return s;
        return null;
    }

